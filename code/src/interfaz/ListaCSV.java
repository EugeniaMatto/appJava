    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        FileFilter filtro = new FileNameExtensionFilter("Archivos (.csv)", "csv");
        fileChooser.setFileFilter(filtro);
        int valor = fileChooser.showOpenDialog(fileChooser);
        if (valor == JFileChooser.APPROVE_OPTION) {
            String ruta = fileChooser.getSelectedFile().getAbsolutePath();

            ArchivoLectura arch = new ArchivoLectura(ruta);

            // si le paso solo la ruta queda "cargando"
            // si le paso el file con la ruta al momento de cargaCSV me aparece un caracter adelante del 1er numero
            rot = new Rotiseria();
            rot.cargaCsv(arch);

            MenuPrincipal m = new MenuPrincipal(rot);
            m.setBounds(10, 20, 1100, 400);
            m.setLocationRelativeTo(null);
            m.setVisible(true);
            dispose();

        } else {
            System.out.println("No se ha seleccionado ning√∫n fichero");
        }
    }//GEN-LAST:event_btnCargarActionPerformed

